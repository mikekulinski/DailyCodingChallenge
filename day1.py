def is_k_sum_available(numbers, k):
    seen = set()
    for n in numbers:
        if (k - n) in seen:
            return True
        seen.add(n)

    return False


def test1():
    numbers = [10, 15, 3, 7]
    k = 17
    assert(is_k_sum_available(numbers, k) == True)

def test2():
    numbers = [1, 2, 4, 8, 13, 2]
    k = 8
    assert(is_k_sum_available(numbers, k) == False)

